document.addEventListener('DOMContentLoaded', function () {
  const downloadBtn = document.getElementById('downloadPdfBtn');
  if (downloadBtn) {
    downloadBtn.addEventListener('click', function () {
      // Check if jsPDF is loaded
      if (!window.jspdf || !window.jspdf.jsPDF) {
        alert('PDF generator library is not loaded. Please try again later.');
        return;
      }
      
      const { jsPDF } = window.jspdf;
      const doc = new jsPDF();
      
      // PDF Title
      doc.setFontSize(22);
      doc.setTextColor(211, 47, 47); // Red color matching theme
      doc.text('GP Crackers - Product Price List', 14, 22);
      
      doc.setFontSize(11);
      doc.setTextColor(100);
      doc.text('Premium quality fireworks for your celebrations.', 14, 30);
      doc.text('Contact: +91 99999 99999 | Email: info@gpcrackers.com', 14, 36);
      
      // Sample Products Data (5 Products: Name, Category, Price)
      const productsData = [
        ['1', 'Color Koti', 'Flower Pots', 'Rs. 150'],
        ['2', '7cm Color Sparklers', 'Sparklers', 'Rs. 80'],
        ['3', 'Lunik Express', 'Rockets', 'Rs. 250'],
        ['4', 'Big Chakkar', 'Ground Chakkar', 'Rs. 100'],
        ['5', 'Chit Put', 'Bombs', 'Rs. 50']
      ];
      
      // Table layout using autoTable
      doc.autoTable({
        startY: 45,
        head: [['S.No', 'Product Name', 'Category', 'Price']],
        body: productsData,
        theme: 'striped',
        headStyles: { fillColor: [211, 47, 47], textColor: [255, 255, 255] },
        styles: { fontSize: 11, cellPadding: 4 },
        alternateRowStyles: { fillColor: [250, 235, 235] }
      });
      
      // Footer
      const pageCount = doc.internal.getNumberOfPages();
      for(let i = 1; i <= pageCount; i++) {
        doc.setPage(i);
        doc.setFontSize(10);
        doc.setTextColor(150);
        const pageSize = doc.internal.pageSize;
        const pageHeight = pageSize.height ? pageSize.height : pageSize.getHeight();
        doc.text('Generated by GP Crackers - www.gpcrackers.com', 14, pageHeight - 10);
      }
      
      // Save the PDF
      doc.save('GP-Crackers-Products.pdf');
    });
  }
});
